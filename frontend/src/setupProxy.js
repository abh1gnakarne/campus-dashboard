// setupProxy.js — Development proxy to avoid CORS issues
// This automatically proxies /api/* to localhost:5000 during npm start
const { createProxyMiddleware } = require("http-proxy-middleware");

module.exports = function (app) {
  app.use(
    "/api",
    createProxyMiddleware({
      target: "http://localhost:5000",
      changeOrigin: true,
    })
  );
};
